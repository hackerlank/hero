package com.joymeng.game.domain.friend;

public class Enemy {//仇人
	int id;
	int myId;//我的id
	int enemyId;//仇人的id
	long attackTime;//攻击时间
	String reason;//仇敌原因
	/**
	 * @return GET the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param SET id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return GET the myId
	 */
	public int getMyId() {
		return myId;
	}
	/**
	 * @param SET myId the myId to set
	 */
	public void setMyId(int myId) {
		this.myId = myId;
	}
	/**
	 * @return GET the enemyId
	 */
	public int getEnemyId() {
		return enemyId;
	}
	/**
	 * @param SET enemyId the enemyId to set
	 */
	public void setEnemyId(int enemyId) {
		this.enemyId = enemyId;
	}
	/**
	 * @return GET the attackTime
	 */
	public long getAttackTime() {
		return attackTime;
	}
	/**
	 * @param SET attackTime the attackTime to set
	 */
	public void setAttackTime(long attackTime) {
		this.attackTime = attackTime;
	}
	/**
	 * @return GET the reason
	 */
	public String getReason() {
		return reason;
	}
	/**
	 * @param SET reason the reason to set
	 */
	public void setReason(String reason) {
		this.reason = reason;
	}
}

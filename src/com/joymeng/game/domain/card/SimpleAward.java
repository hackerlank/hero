package com.joymeng.game.domain.card;

public class SimpleAward {

	public static int GOLD = -1;
	
	public static int HONOR = -2;
	
	public static int TURN_TIMES = -3;
	/**
	 * 奖品是什么东西，-1--金币，-2--功勋，-3--旋转次数，正数表示物品id
	 */
	private int what;
	
	/**
	 * 奖品数量
	 */
	private int value;
	

	public int getWhat() {
		return what;
	}

	public void setWhat(int what) {
		this.what = what;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	
}

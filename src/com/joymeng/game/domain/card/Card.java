package com.joymeng.game.domain.card;

public class Card {

	/**
	 * 面值
	 */
	private byte face;
	
	/**
	 * 下标
	 */
	private byte index;
	
	/**
	 * 是什么 ，-1--金币，-2--功勋，-3--旋转次数，正数表示物品id
	 */
	private int what;
	
	/**
	 * 抽中的数量
	 */
	private int value;

	public byte getFace() {
		return face;
	}

	public void setFace(byte face) {
		this.face = face;
	}

	public byte getIndex() {
		return index;
	}

	public void setIndex(byte index) {
		this.index = index;
	}

	public int getWhat() {
		return what;
	}

	public void setWhat(int what) {
		this.what = what;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	
}

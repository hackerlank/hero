package com.joymeng.game.domain.role;
/**
 * 玩家级别限制
 * @author admin
 * @date 2012-5-30
 * TODO
 */
public class PlayerLimit {
	int level;
	int heroNum;
	int awardLevel;
	int cityNum;
	int levelExp;
	/**
	 * @return GET the levelExp
	 */
	public int getLevelExp() {
		return levelExp;
	}
	/**
	 * @param SET levelExp the levelExp to set
	 */
	public void setLevelExp(int levelExp) {
		this.levelExp = levelExp;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getHeroNum() {
		return heroNum;
	}
	public void setHeroNum(int heroNum) {
		this.heroNum = heroNum;
	}
	public int getAwardLevel() {
		return awardLevel;
	}
	public void setAwardLevel(int awardLevel) {
		this.awardLevel = awardLevel;
	}
	public int getCityNum() {
		return cityNum;
	}
	public void setCityNum(int cityNum) {
		this.cityNum = cityNum;
	}
	

}

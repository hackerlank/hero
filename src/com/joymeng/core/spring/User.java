package com.joymeng.core.spring;

/**
 * 用户对象
 * */
public class User {
	
		
		/**
		 * 姓名
		 * */
		private String name;
		
		/**
		 * 所在公司
		 * */
		private Company company;
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public Company getCompany() {
			return company;
		}
		public void setCompany(Company company) {
			this.company = company;
		}

}
